#ifndef _FUNCTION_LIKE_MACRO_H_
#define _FUNCTION_LIKE_MACRO_H_

/**
 * File name: 3-function_like_macro.h
 * Description: Macro function that prints the absolute value
 * @x: number to be used
 */

#define ABS(x) ((x < 0) ? (-x) : (x)) /* -x is evaluated to +x */

#endif /* _FUNCTION_LIKE_MACRO_H */
